package com.faikturan;

public class Sample009 {

	public static void main(String[] args) {
		int sonuc;
		boolean basari = false;
		
		sonuc = +1;//Sonuc 1 oldu;
		System.out.println(sonuc);
		sonuc--;//Sonuc 0 oldu;
		System.out.println(sonuc);
		sonuc++;//Sonuc 1 oldu
		System.out.println(sonuc);
		sonuc =-sonuc;//Sonuc -1 oldu
		System.out.println(sonuc);
		System.out.println(basari);
		System.out.println(!basari);
	}

}
